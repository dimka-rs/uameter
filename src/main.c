/* callbacks */
void HAL_ADC_ConvCpltCallback(ADC_HandleTypeDef* hadc) {
	uint32_t adc_res = HAL_ADC_GetValue(hadc);
	USB_BUF[0] = uptime;
	USB_BUF[1] = adc_res;
	/* set 15th bit to indicate low current mode */
	if (low_current == 1) {
		USB_BUF[1] += 1 << 15U;
	}
	CDC_Transmit_FS((uint8_t*)USB_BUF, sizeof(uint32_t)*2);
	/* switch to low current if reading below threshold */
	if ((low_current == 0) && (adc_res < LOW_TRSH)) {
		low_current = 1;
		led_delay = LED_DELAY_FAST;
		HAL_GPIO_WritePin(RANGE_GPIO_Port, RANGE_Pin, GPIO_PIN_SET);
	}
	/* switch to high current if reading above threshold */
	if ((low_current == 1) && (adc_res > HIGH_TRSH)) {
		low_current = 0;
		led_delay = LED_DELAY_SLOW;
		HAL_GPIO_WritePin(RANGE_GPIO_Port, RANGE_Pin, GPIO_PIN_RESET);
	}
}

